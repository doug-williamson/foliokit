/**
 * Admin — auth flow tests.
 *
 * These tests verify unauthenticated redirect, sign-in, and sign-out behaviour.
 * They intentionally run WITHOUT the default storageState so the user starts
 * as a fresh, unauthenticated visitor.
 */

import { test, expect } from '@playwright/test';

// Override the project-level storageState so we start unauthenticated.
test.use({ storageState: { cookies: [], origins: [] } });

test.describe('unauthenticated access', () => {
  test('redirects / to /login', async ({ page }) => {
    await page.goto('/');
    await expect(page).toHaveURL(/\/login/, { timeout: 10_000 });
  });

  test('redirects /posts to /login', async ({ page }) => {
    await page.goto('/posts');
    await expect(page).toHaveURL(/\/login/, { timeout: 10_000 });
  });

  test('redirects /about-page to /login', async ({ page }) => {
    await page.goto('/about-page');
    await expect(page).toHaveURL(/\/login/, { timeout: 10_000 });
  });

  test('redirects /links-page to /login', async ({ page }) => {
    await page.goto('/links-page');
    await expect(page).toHaveURL(/\/login/, { timeout: 10_000 });
  });

  test('shows the login page with sign-in button', async ({ page }) => {
    await page.goto('/login');
    await expect(page.getByRole('heading', { name: 'FolioKit' })).toBeVisible();
    await expect(page.getByRole('button', { name: /sign in with google/i })).toBeVisible();
  });
});

